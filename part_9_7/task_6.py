# –¢—Ä–µ–±—É–µ—Ç—Å—è –Ω–∞–ø–∏—Å–∞—Ç—å –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –Ω–∞—Ö–æ–¥–∏—Ç —Å—Ç–æ–∏–º–æ—Å—Ç—å —Å—Ç–∞—Ä–æ–≥–æ –∏ –Ω–æ–≤–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏–π –°—ç–º–∞ –≤ üêù –∏ –≤—ã–≤–æ–¥–∏—Ç —Ç–µ–∫—Å—Ç –≤ —Å–ª–µ–¥—É—é—â–µ–º —Ñ–æ—Ä–º–∞—Ç–µ:
#–°—Ç–∞—Ä–∞—è —Å—Ç–æ–∏–º–æ—Å—Ç—å: <—Å—Ç–æ–∏–º–æ—Å—Ç—å —Å—Ç–∞—Ä–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è>üêù
#–ù–æ–≤–∞—è —Å—Ç–æ–∏–º–æ—Å—Ç—å: <—Å—Ç–æ–∏–º–æ—Å—Ç—å –Ω–æ–≤–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è>üêù

typed_msg_en = input()
msg_price_old = 0
msg_price_new = 0
ru_letters = '–µ—É–æ—Ä–∞—Ö—Å–ï–¢–û–†–ê–ù–•–°–í–ú'
en_letters = 'eyopaxcETOPAHXCBM'
for symbol in typed_msg_en:
    msg_price_old += ord(symbol)
sam_earn_en = msg_price_old * 3
print(f'Old price: {sam_earn_en}üêù')
#check eng letters
for symbol in typed_msg_en:
    if symbol in en_letters:
        en_index = en_letters.index(symbol)
        ru_index = en_index
        ru_letter = ru_letters[ru_index]
        msg_price_new += ord(ru_letter) * 3
    else:
        msg_price_new += ord(symbol) * 3
print(f'New price: {msg_price_new}üêù')












